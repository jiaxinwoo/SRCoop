package com.srcoop.android.activity.bean;

import cn.bmob.v3.BmobUser;
import cn.bmob.v3.datatype.BmobFile;
import cn.bmob.v3.datatype.BmobRelation;

public class MyUser extends BmobUser {


	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String mName;
	private String mTel;
	private BmobFile mPhoto;
	private BmobRelation mUser_EventNotices;
	private int mType;
		
	public String getName() {return mName;}
	public void setName(String name) {this.mName = name;}

	public String getTel() {return mTel;}
	public void setTel(String tel) {this.mTel = tel;}
	
	public int getType() {return mType;}
	public void setType(int Type) {this.mType = Type;}
	
	public BmobFile getPhoto() {return mPhoto;}
	public void setPhoto(BmobFile photo) {this.mPhoto = photo;}
	
	public BmobRelation getUser_EventNotices() {return mUser_EventNotices;}
	public void setUser_EventNotices(BmobRelation user_eventnotices) {this.mUser_EventNotices = user_eventnotices;}
	
    @Override
    public String toString()
    {return "";}
}
