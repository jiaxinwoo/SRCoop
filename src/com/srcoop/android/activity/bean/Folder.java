package com.srcoop.android.activity.bean;

import cn.bmob.v3.BmobObject;
import cn.bmob.v3.datatype.BmobRelation;

public class Folder extends BmobObject{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String mName;
	private BmobObject mParent;
	private BmobRelation mChildren;
	
	//对neme（文件名）的操作
	public String getName(){return mName;}	
	public void setName(String name){this.mName=name;}
	
	public BmobObject getParent(){return mParent;}
	public void setParent(BmobObject parent){this.mParent=parent;}
	
	public BmobRelation getChildren(){return mChildren;}
	public void setChildren(BmobRelation children){this.mChildren=children;}
	
    @Override
    public String toString()
    {return "";}
}
